class JavaSample
{
	public static void main(String args[])
	{
		System.out.println("Hello");
		System.out.println("Sum="+sum(10,20));
		System.out.println("Product="+multiply(10,20));
	}

	public int sum(int a, int b)
	{
		int c= a+b;
		return c;
	}

	public multiply(int a, int b)
	{
		int c=a*b;
		return c;
	}
}